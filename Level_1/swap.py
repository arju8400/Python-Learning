

# This is a simple example showing how to swap two values in Python.
# A beginner can understand it easily.

# Example values
x = 5
y = 10

print("Before swap:")
print("x =", x)
print("y =", y)

# Swap the values using tuple unpacking
x, y = y, x

print("\nAfter swap:")
print("x =", x)
print("y =", y)
